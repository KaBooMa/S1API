namespace S1API.PhoneCalls.Constants
{
    /// <summary>
    /// Defines the types of system triggers that can be associated with a phone call stage.
    /// </summary>
    public enum SystemTriggerType
    {
        /// <summary>
        /// Represents triggers that execute when the phone call stage starts.
        /// </summary>
        StartTrigger,
        /// <summary>
        /// Represents triggers that execute when the phone call stage completes.
        /// </summary>
        DoneTrigger
    }
}
